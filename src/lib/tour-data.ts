const getImage = (id: string) => {
  const image = PlaceHolderImages.find((img) => img.id === id);
  if (!image) {
    // Возвращаем заглушку, чтобы данные туров не ломали приложение при отсутствии ID в JSON
    return {
      id,
      imageUrl: '',
      description: '',
      imageHint: ''
    };
  }
  return image;
};

